class SwitchDemo{
	public static void main(String[] args){
		int num=1;
		switch(num){
			case 1:
				 System.out.println("Statement 1");
			break;	 
			case 2:
				 System.out.println("Statement 2");
			break;
			case 3:
				 System.out.println("Statement 3");
		 	break;
			default:
		 System.out.println("Default State");
		}
	}
}	
